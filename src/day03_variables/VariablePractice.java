package day03_variables;

public class VariablePractice {
    public static void main(String[] args) {
        byte age= 35; /* age is here the variable name and assigning 35 value.*/
        System.out.println(age);// printing the age variable, which means I will print the value of the variable
        System.out.println("age");//this prints the characters age, not the variable that has a value
        System.out.println("35");// printing the character 3 and 5
        System.out.println(35);//printing the int 35.
        // by default 35 is int type
        System.out.println(age+1);// age value is 35, then it adds+1 and prints 36.
    }
}
