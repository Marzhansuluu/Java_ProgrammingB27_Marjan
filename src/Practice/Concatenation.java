package Practice;

public class Concatenation {
    public static void main(String[] args) {

        String language= "Java";
        // print; My favorite programming language is ....
        System.out.println("My favorite programming language is "+language+"."); //concatenation is basically + sign.

        System.out.println("----------------------------------------------");
        String name="John";
        int age=35;
        // print: My name ...., and I am ..... years old.
        System.out.println("My name is "+ name + ", and I am " + age + " years old.");
    }
}
